// 애스펙트 키 생성 헬퍼 (행성 이름 정렬)
export function getAspectKey(planet1: string, planet2: string): string {
  const order = ['태양', '달', '수성', '금성', '화성', '목성', '토성', '천왕성', '해왕성', '명왕성']
  const i1 = order.indexOf(planet1)
  const i2 = order.indexOf(planet2)
  return i1 <= i2 ? `${planet1}-${planet2}` : `${planet2}-${planet1}`
}
